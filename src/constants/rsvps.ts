export const getAddr = (name: string): string => rsvps.addrs[rsvps.n2i[name]];

export const getNameAddr = (
  name: string
): { name: string; addr: string } | null => {
  const idx = rsvps.n2i[name];
  if (idx === undefined) return null;
  return {
    name,
    addr: rsvps.addrs[idx],
  };
};

export const getAddr2Names = () => {
  const a2n: Record<string, string[]> = {};
  Object.entries(rsvps.n2i).forEach(([name, idx]) => {
    const addr = rsvps.addrs[idx];
    if (!a2n[addr]) a2n[addr] = [];
    a2n[addr].push(name);
  });
  return a2n;
};
